from urllib.parse import parse_qs, urlparse

from elody.policies.helpers import get_flat_item_and_object_lists
from flask import g, request
from flask_restful import Headers
from inuits_policy_based_auth import RequestContext
from policy_factory import authenticate
from resources.base_resource import BaseResource
from resources.elody._blueprint import api
from resources.elody.document import ElodyDocument
from resources.elody.document_relations import ElodyDocumentRelations
from resources.elody.documents import ElodyDocuments
from resources.elody.filter import ElodyFilter
from werkzeug.exceptions import Conflict
from werkzeug.wrappers import Response


